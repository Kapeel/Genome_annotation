#-----------------------------------------------------------------------------
#----------------------------------FUNCTIONS----------------------------------
#-----------------------------------------------------------------------------
sub build_lu{
    my $file = shift;
    my %lu;
    my $fh = new FileHandle;
    $fh->open($file);
    
    while (defined(my $line = <$fh>)){
        chomp($line);
	$lu{$line}=1;
    }
    $fh->close();
    return (\%lu);
}
#-----------------------------------------------------------------------------
sub build_match_lu{
    my $file = shift;
    my %lu;
    my $fh = new FileHandle;
    $fh->open($file);

    while (defined(my $line = <$fh>)){
        chomp($line);
	my @stuff = split(/\t/, $line);
        $lu{$stuff[0]}=$stuff[1];
    }
    $fh->close();
    return (\%lu);

}
